# Format one cell; bold if outside nominal band
.cell <- function(x) {
  if (is.na(x)) return("--")
  s <- sprintf("%.4f", x)
  if (abs(x - 0.95) > 2 * MC_SE) s <- paste0("\\textbf{", s, "}")
  s
}

# Build an 8-column tabularx body
# mat : nrow = length(n_vals), ncol = 2 * length(M_vals)
# lh  : left panel header (LaTeX string, no \\ at end)
# rh  : right panel header
.tabularx <- function(mat, lh, rh) {
  header <- paste0(
    "\\setlength{\\tabcolsep}{4.2pt}\n",
    "\\begin{tabularx}{\\linewidth}{@{}l rrrr @{\\quad} rrrr@{}}\n",
    "\\toprule\n",
    "& \\multicolumn{4}{c}{", lh, "}\n",
    "& \\multicolumn{4}{c}{", rh, "} \\\\\n",
    "\\cmidrule(lr){2-5}\\cmidrule(lr){6-9}\n",
    "$n$ & $M{=}1$ & $M{=}2$ & $M{=}5$ & $M{=}10$\n",
    "    & $M{=}1$ & $M{=}2$ & $M{=}5$ & $M{=}10$ \\\\\n",
    "\\midrule"
  )
  nc   <- length(M_vals)
  rows <- vapply(seq_along(n_vals), function(ni) {
    vals <- c(sapply(seq_len(nc),      function(j) .cell(mat[ni, j])),
              sapply(seq_len(nc) + nc, function(j) .cell(mat[ni, j])))
    paste0(n_vals[ni], " & ",
           paste(vals, collapse = " & "), " \\\\")
  }, character(1L))
  footer <- "\\botrule\n\\end{tabularx}"
  paste(c(header, rows, footer), collapse = "\n")
}

