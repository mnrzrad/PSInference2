cols <- c("#2166AC","#4DAC26","#D6604D","#7B2D8B")
pchs <- c(16,17,15,18)
ltys <- c(1,2,4,5)
M_lab <- paste0("M = ", M_vals)
band_lo <- 0.95 - 2*MC_SE
band_hi <- 0.95 + 2*MC_SE

.add_band <- function() {
  usr <- par("usr")
  rect(usr[1], band_lo, usr[2], band_hi,
       col = adjustcolor("grey70", 0.35), border = NA)
  abline(h = 0.95, lwd = 1.5, lty = 2, col = "grey40")
}

.cov_panel <- function(mat_cols, main_expr,
                       ylim = c(0.928, 0.972),
                       show_legend = FALSE) {
  plot(NA, xlim = c(1, length(n_vals)), ylim = ylim,
       xaxt = "n", xlab = expression(italic(n)),
       ylab = "Coverage", main = main_expr,
       las = 1, cex.main = 0.88, cex.axis = 0.82,
       cex.lab = 0.88)
  axis(1, at = seq_along(n_vals), labels = n_vals, cex.axis = 0.82)
  .add_band()
  for (mi in seq_along(M_vals)) {
    y  <- mat_cols[[mi]]
    ok <- !is.na(y)
    if (any(ok))
      lines(which(ok), y[ok],
            col = cols[mi], lwd = 2, lty = ltys[mi],
            type = "b", pch = pchs[mi], cex = 1.15)
  }
  if (show_legend)
    legend("bottomright", M_lab,
           col = cols, lty = ltys, pch = pchs,
           pt.cex = 1.1, lwd = 2, bty = "n", cex = 0.76)
}

